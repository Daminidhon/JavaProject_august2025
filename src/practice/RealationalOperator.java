package practice;

public class RealationalOperator {

	public static void main(String[] args) {
		
		int p=20;    //hard coding
		int t=39;   //hard coding
		boolean result= (p==t);
		
		System.out.println(p);
		System.out.println(t);
		System.out.println(result);
		
		result=(p>t);
		System.out.println(result);
		
		result=(p>=t);
		System.out.println(result);
		
		result=(p<t);
		System.out.println(result);
		
		result=(p<=t);
		System.out.println(result);
		
		result=(p!=t);
		System.out.println(result);

	}

}
