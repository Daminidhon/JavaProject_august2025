package practice;

public class UnaryOperator {

	public static void main(String[] args) {
		
		int e =5;
		e++;    // e= e+1
		 System.out.println(e);
		 
		int r =34;
		r--;    // r=r-1
		System.out.println(r);
		
		boolean t= false;
		System.out.println(t);
		System.out.println(!t);
		
		boolean y= false;
		boolean u= !y;
		System.out.println(y);
		System.out.println(u);
		
		
	}
	

}
