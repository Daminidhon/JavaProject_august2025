package practice;

public class DataTypeDemo1 {

	public static void main(String[] args) {
		int f2 = 5;                                             // Integer (whole number), 4 byte
		float height = 5.9756768778998888889f;                                  // Floating point number, 4 byte
		char mysection = 'B';                                 // Character, 2 byte
		boolean Isitraining = true;                          // Boolean, 1b
		String quote = "a stitch in time save the 9 ";      // String, not fix
		System.out.println(f2+2);
		System.out.println(height);
		System.out.println(mysection);
		System.out.println(Isitraining);
		System.out.println(quote);
		double width =  5.9756768778998888889;
		System.out.println(width);
		byte age =43;  // -128 to 127
		System.out.println(age);
		short distance =-1012;
		System.out.println(distance);

	}

}
